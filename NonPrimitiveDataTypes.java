public class NonPrimitiveDataTypes {
    public static void main(String args[]){
        // printing an array element
        int arr[]; // declaring an array
        arr = new int[3]; // declaring the size
        arr[0] = 12;
        arr[1]= 12;
        arr[2] = 3;
        System.out.println(arr[2]);

        // printing an string
        String a = "Aditya";
        System.out.print(a);

    }
}
